public class PuntoCinco {
   
   public static void main(String[] args) {
   
      int A,B,C,AUX;
      
      A=1;
      System.out.println("A="+A);
      B=2;
      System.out.println("B="+B);
      C=3;
      System.out.println("C="+C);
      
      AUX=A;
      A=C;
      System.out.println("A="+A);
      B=AUX;
      System.out.println("B="+B);
      C=AUX;
      System.out.println("C="+C);
      
      
   
   
   }
}